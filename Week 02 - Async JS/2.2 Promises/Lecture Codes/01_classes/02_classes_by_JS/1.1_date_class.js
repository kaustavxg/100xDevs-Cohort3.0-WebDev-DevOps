// ========= JAVASCRIPT gives some CLASSES of its own ============


const date = new Date();
console.log(`Current year: ${date.getFullYear()}`); // print the current year

console.log(`Current date: ${date.getDate()}`); // print the current date

console.log(`Current hour: ${date.getHours()}`); // print the current hour

console.log(`Current day: ${date.getDay()}`); // print the current day number

console.log(`Current minute: ${date.getMinutes()}`); // print the current minute

console.log(`Current date and time: ${date}`); // print the time and date in UTCf

console.log(`Current month: ${date.getMonth()}`); // indexing from 0 not 1



