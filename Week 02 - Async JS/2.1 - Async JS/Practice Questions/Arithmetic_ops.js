// ADD
function sum(a, b) {
    return a+b;
}
let ans = sum(4, 6);
console.log(ans);

// MINUS
function subtract(a, b) {
    return a-b;
} 
let ans2 = subtract(6, 2);
console.log(ans2);

// MULTIPLY 
function pro(a, b) {
    return a*b;
}
console.log(pro(3, 5)); // we can write it directly
let ans3 = pro(3, 5); // this is a cleaner way 
console.log(ans3);

