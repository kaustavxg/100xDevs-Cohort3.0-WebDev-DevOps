// BASIC CALCULATOR FUNCTIONS

// Addition function
function sum(a, b) {
    return a + b;
}

// Subtraction function
function subtract(a, b) {
    return a - b;
}

// Multiplication function
function multiply(a, b) {
    return a * b;
}

// Division function
function divide(a, b) {
    return a / b;
}

// Test all functions
const ans = sum(1, 2);
const ans2 = subtract(1, 2);
const ans3 = multiply(1, 2);
const ans4 = divide(1, 2);

console.log(ans);  // Output: 3
console.log(ans2); // Output: -1
console.log(ans3); // Output: 2
console.log(ans4); // Output: 0.5